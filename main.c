//-----------------------------------------------------------------------------------------------------------------------//
void dibujar_mapa(char mapa[MAPA_FILAS][MAPA_COLUMNAS])
{
  tb_clear();

  int x = 0;
  int y = 0;

  //recorrer e imprimir una seccion de 20x20 del mapa 
  for (int i = zona_top; i < zona_fondo; i++) //recorre las primeras 20 filas de la seccion
  {
    for (int j = 0; j < MAPA_COLUMNAS; j++) //recorre las 20 columnas de la seccion
    {
      if (i==jugador_posY && j==jugador_posX)
      {
        tb_set_cell(x*2,y,'P',TB_WHITE,TB_DEFAULT);
      }
      else
      {
        tb_set_cell(x*2,y,mapa[i][j],TB_GREEN,TB_DEFAULT);
      }
      x++;
    }
    x=0;
    y++;
  }
  tb_present();
}
//-----------------------------------------------------------------------------------------------------------------------//
void caminar(char mapa[MAPA_FILAS][MAPA_COLUMNAS])
{
  struct tb_event ev;
  int sig_JPosX = jugador_posX;
  int sig_JPosY = jugador_posY;

  while (1)
  {
    tb_poll_event(&ev);
    if (ev.type == TB_EVENT_KEY)
    {
      if (ev.ch)
      {
        switch (ev.ch)
        {
        case 'w':
          sig_JPosY--;
          break;
        
        case 'a':
          sig_JPosX--;
          break;

        case 's':
          sig_JPosY++;
          break;

        case 'd':
          sig_JPosX++;
          break;

        case 'q':
          win = 1;
          break;
        
        default:
          break;
        }
        break;
      } 
    }
  }

  total_pasos++;

  //si es una pared, no se mueve a la nueva direccion
  if(validar_movimiento_NASM(&mapa[0][0],MAPA_COLUMNAS,sig_JPosY,sig_JPosX))
  {
    return;
  }

  //si no es pared, entonces checar que es 
  switch (mapa[sig_JPosY][sig_JPosX])
  {

  case '$':
    monedas_colectadas++;
    mapa[sig_JPosY][sig_JPosX] = ' ';
    jugador_posX = sig_JPosX;
    jugador_posY = sig_JPosY;
    break;

  case ' ':
    jugador_posX = sig_JPosX;
    jugador_posY = sig_JPosY;
    break;

  case '>':
    zona_top += 20;
    zona_fondo += 20;
    jugador_posX = 1;
    jugador_posY = 21;
    break;

  default:
    break;
  }
}
