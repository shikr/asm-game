void caminar()
{
  struct tb_event ev;

  while (1)
  {
    tb_poll_event(&ev);
    if (ev.type == TB_EVENT_KEY)
    {
      if (ev.ch)
      {
        switch (ev.ch)
        {
        case 'w':
          //printf("adelante\n");
          jugador_posY--;
          break;
        
        case 'a':
         // printf("izq\n");
         jugador_posX--;
          break;

        case 's':
          //printf("atras\n");
          jugador_posY++;
          break;

        case 'd':
          //printf("der\n");
          jugador_posX++;
          break;

        case 'q':
        win = 1;
        break;
        
        default:
          break;
        }
        break;
      } 
    }
  }
}
