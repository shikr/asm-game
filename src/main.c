void caminar(char mapa[MAPA_FILAS][MAPA_COLUMNAS])
{
  struct tb_event ev;
  int sig_JPosX = jugador_posX;
  int sig_JPosY = jugador_posY;

  while (1)
  {
    tb_poll_event(&ev);
    if (ev.type == TB_EVENT_KEY)
    {
      if (ev.ch)
      {
        switch (ev.ch)
        {
        case 'w':
          sig_JPosX--;
          break;
        
        case 'a':
          sig_JPosY--;
          break;

        case 's':
          sig_JPosX++;
          break;

        case 'd':
          sig_JPosY++;
          break;

        case 'q':
          win = 1;
          break;
        
        default:
          break;
        }
        break;
      } 
    }
  }

  switch (mapa[sig_JPosX][sig_JPosY])
  {
  case '#':
    break;

  case '$':
    monedas_colectadas++;
    mapa[sig_JPosX][sig_JPosY] = ' ';
    jugador_posX = sig_JPosX;
    jugador_posY = sig_JPosY;
    break;

  case ' ':
    jugador_posX = sig_JPosX;
    jugador_posY = sig_JPosY;
    break;

  case '>':
    zona_top += 20;
    zona_fondo += 20;
    break;

  default:
    break;
  }
}
